# area_module.py
"""functions to calculate area of a rectangle, square and circle"""  # docstring


# Functions
def rec(l, b):
    """returns area of rectangle"""
    return l * b


def sq(s):
    """returns area of square"""
    return s * s


def circle(r):
    """returns area of circle"""
    return pi * r * r


# CONSTANTS
pi = 22 / 7
